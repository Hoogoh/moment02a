r = float(input("Skriv radien av cirkeln i cm: ")) #Här gör jag så att man kan skriva in radien av cirkeln

pi=3.14

omkrets=2*pi*r

area=pi*r*r
#Formlerna för hur man räknar ut en cirkels area och omkrets

print("Area av cirkeln: ",f"{area:5.2f}" "cm\u00b2")     # \u00b2 gör så att svaret blir kvadrat

print("Omkretsen av cirkeln: ",f"{omkrets:5.2f}" "cm")